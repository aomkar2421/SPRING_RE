package com.om.CI;

public class Address {
	
	private String city;
	private int pin;
	public Address(String city, int pin) {
		super();
		this.city = city;
		this.pin = pin;
	}
	@Override
	public String toString() {
		return "Address [city=" + city + ", pin=" + pin + "]";
	}
	
	
	
}
